void main() {
  // Deklarasi variabel dengan tipe data dynamic
  dynamic variable = 200;

  // Mencetak nilai variabel variable
  print(variable);

  // Mengubah nilai variabel variable menjadi boolean
  variable = true;

  // Mencetak nilai variabel variable setelah diubah
  print(variable);

  // Mengubah nilai variabel variable menjadi string
  variable = 'Ewaaaa';

  // Mencetak nilai variabel variable setelah diubah
  print(variable);
}
