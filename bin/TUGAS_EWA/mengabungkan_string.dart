void main() {
  // Deklarasi variabel dengan tipe data String
  String firstName = 'ULTRA';
  String lastName = 'HYPER MUTEKI';

  // Menggabungkan dua string menggunakan operator +
  var name1 = firstName + ' ' + lastName;

  // Menggabungkan beberapa string menggunakan interpolasi string
  var name2 = 'EWA ' + 'RISE ' + 'Pasifik';

  // Mencetak nilai variabel name1
  print(name1);

  // Mencetak nilai variabel name2
  print(name2);
}
