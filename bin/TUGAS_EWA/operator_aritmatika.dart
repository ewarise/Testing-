void main() {
  // Penjumlahan
  var result1 = 10 + 10;

  // Pengurangan
  var result2 = 100 - 10;

  // Negasi
  var result3 = -5;

  // Perkalian
  var result4 = 5 * 5;

  // Pembagian
  var result5 = 10 / 3;

  // Pembagian integer
  var result6 = 10 ~/ 3;

  // Modulo
  var result7 = 10 % 3;

  // Mencetak hasil operasi aritmatika
  print('Penjumlahan: $result1');
  print('Pengurangan: $result2');
  print('Negasi: $result3');
  print('Perkalian: $result4');
  print('Pembagian: $result5');
  print('Pembagian Integer: $result6');
  print('Modulo: $result7');
}
